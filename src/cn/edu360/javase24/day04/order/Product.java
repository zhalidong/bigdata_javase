public class Product {
	public String pId;
	public String pName;
	public float price;
	public int number;
	
	public Product(){}

	public Product(String pId, String pName, float price, int number) {
		this.pId = pId;
		this.pName = pName;
		this.price = price;
		this.number = number;
	}
	
	
	
	
	
}
